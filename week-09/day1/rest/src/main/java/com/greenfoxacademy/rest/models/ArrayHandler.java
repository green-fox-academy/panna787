package com.greenfoxacademy.rest.models;

public class ArrayHandler {

    private String what;
    private int[] numbers;

    public ArrayHandler() {
    }

    public String getWhat() {
        return what;
    }

    public void setWhat(String what) {
        this.what = what;
    }

    public int[] getNumbers() {
        return numbers;
    }

    public void setNumbers(int[] numbers) {
        this.numbers = numbers;
    }
}
